#!/usr/bin/env python3
"""Deterministic citation-anchor verifier for DingDuff cite-check.

Reads a proposals file (written by the orchestrating model), verifies every
proposed quote against the stored source files via normalized containment
matching, maps matches back to raw character offsets, and writes cites.json
(schema v1).

Offset semantics: all offsets are Unicode codepoint indices (Python ``str``
indices) into the file content decoded from UTF-8 bytes with NO newline
translation. Consumers slice the raw decoded text; they never re-derive
offsets.

stdout carries exactly one machine-readable JSON report; all diagnostics go
to stderr.

Exit codes:
    0  every citation anchored (or legitimately no_quote_claimed)
    1  one or more retryable anchor failures (cites.json was still written;
       failing citations are marked anchor_failed with a failure_reason)
    2  fatal: malformed proposals, unreadable/undecodable files, duplicate
       citation ids, or other structural errors
"""

from __future__ import annotations

import argparse
import hashlib
import json
import re
import sys
import unicodedata
from dataclasses import dataclass, field
from datetime import datetime, timezone
from pathlib import Path
from typing import Any, Dict, List, Optional, Tuple

SCRIPT_VERSION = "cite-check/verify_anchors.py 1.0"
SCHEMA_VERSION = 1

MIN_SEGMENT_NORM_CHARS = 10
SHORT_QUOTE_NORM_CHARS = 20
HINT_CONTEXT_RAW_CHARS = 200
MAX_PREFIX_SHRINK_WORDS = 30
MAX_CHAIN_START_CANDIDATES = 20

SINGLE_QUOTES = "‘’‚‛′"
DOUBLE_QUOTES = "“”„‟″«»"
DASHES = "‐‑‒–—―−"
ELLIPSIS = "…"

# HTML character references. CourtListener text (notably scraped state
# opinions — S.W. reporters and the like) carries literal &amp; / &#160; /
# &#13; into stored markdown. Decoding during normalization lets a clean
# quote match an entity-bearing source and vice versa.
#
# LOCKSTEP RULE: this named table and the numeric forms must match the
# viewer's JS port exactly (shared vectors in tests/normalization_vectors.json).
# Deliberately a fixed small table — NOT html.unescape's ~2000-name table —
# because the JS side must mirror it byte for byte.
HTML_ENTITIES = {
    "amp": "&", "lt": "<", "gt": ">", "quot": '"', "apos": "'",
    "nbsp": " ", "shy": "­",
    "sect": "§", "para": "¶", "deg": "°", "copy": "©", "reg": "®",
    "trade": "™", "middot": "·", "bull": "•",
    "mdash": "—", "ndash": "–", "hellip": "…",
    "ldquo": "“", "rdquo": "”", "lsquo": "‘", "rsquo": "’",
    "prime": "′", "Prime": "″",
    "ensp": " ", "emsp": " ", "thinsp": " ",
    "frac12": "½", "frac14": "¼", "frac34": "¾",
}
ENTITY_RE = re.compile(
    r"&(?:#(\d{1,7})|#[xX]([0-9a-fA-F]{1,6})|([a-zA-Z][a-zA-Z0-9]{1,30}));")

CITATION_ID_RE = re.compile(r"^c\d{3,}$")
BODY_END_RE = re.compile(r"^## Citing Cases", re.MULTILINE)
FOOTER_RE = re.compile(r"^\*Generated by DingDuff", re.MULTILINE)
WORD_HYPHEN_RE = re.compile(r"(?<=\w)-(?=\w)")
# Visible page-boundary markers that opinion_store inserts inline (replacing
# CL's invisible U+200B) — treated as zero-width during matching, the same
# way Cf chars (ZWSPs etc.) are handled below. Keep this regex in lockstep
# with PAGE_MARKER_RE in review_viewer.html.
PAGE_MARKER_RE = re.compile(r"<<pg\. \d+>>")


# ---------------------------------------------------------------------------
# Normalization
# ---------------------------------------------------------------------------

def _is_dropped(ch: str) -> bool:
    """Format characters (Cf) — soft hyphens, zero-width spaces, BiDi marks —
    are invisible and must not affect matching."""
    return unicodedata.category(ch) == "Cf"


def _decode_entity_at(text: str, i: int) -> Tuple[str, int]:
    """Effective character at position i: (decoded_char, raw_width).

    Decodes one HTML character reference if present (single pass — never
    re-decodes, so '&amp;#160;' yields the literal text '&#160;'); otherwise
    returns the literal character with width 1. Unknown names and invalid
    codepoints stay literal, so 'AT&T;' and 'R&D' are untouched.
    """
    if text[i] == "&":
        m = ENTITY_RE.match(text, i)
        if m:
            if m.group(3) is not None:
                named = HTML_ENTITIES.get(m.group(3))
                if named is not None:
                    return named, m.end() - i
            else:
                cp = int(m.group(1)) if m.group(1) is not None else int(m.group(2), 16)
                if 0 < cp <= 0x10FFFF and not (0xD800 <= cp <= 0xDFFF):
                    return chr(cp), m.end() - i
    return text[i], 1


def normalize_with_map(text: str) -> Tuple[str, List[int], List[int]]:
    """Normalize text for matching; return (normalized, starts, ends) where
    raw[starts[i]:ends[i]] is the raw span that produced normalized char i.

    Spans (rather than the previous single-index map) are required because an
    HTML character reference maps MANY raw chars to one normalized char; the
    old `idx[e-1] + 1` end convention would cut a trailing entity in half.

    Rules (in order): decode HTML character references; drop Cf chars;
    collapse Unicode whitespace runs (including whitespace entities) to one
    space; fold dashes to '-' with hyphen-linebreak de-hyphenation; fold
    curly quotes; expand the ellipsis char; per-char NFKC for the rest.
    """
    out: List[str] = []
    starts: List[int] = []
    ends: List[int] = []
    i, n = 0, len(text)
    while i < n:
        # Skip `<<pg. N>>` page markers entirely — contribute zero normalized
        # chars and no entries to starts/ends, so they're invisible to
        # quote matching but the position map still maps each emitted char
        # back to its true raw offset (past the marker).
        if text.startswith("<<pg. ", i):
            pm = PAGE_MARKER_RE.match(text, i)
            if pm:
                i = pm.end()
                continue
        ch, w = _decode_entity_at(text, i)
        if _is_dropped(ch):
            i += w
            continue
        if ch.isspace():
            run_start = i
            i += w
            while i < n:
                c2, w2 = _decode_entity_at(text, i)
                if c2.isspace() or _is_dropped(c2):
                    i += w2
                else:
                    break
            if out and out[-1] == " ":
                continue
            out.append(" ")
            starts.append(run_start)
            ends.append(i)
            continue
        if ch == "-" or ch in DASHES:
            # Hyphen-linebreak de-hyphenation: a hyphen directly after a
            # letter, followed by whitespace containing a newline and then a
            # lowercase letter, is a typesetting artifact — drop it and the
            # whitespace so "expecta-\ntion" matches "expectation".
            j = i + w
            saw_newline = False
            while j < n:
                c3, w3 = _decode_entity_at(text, j)
                if c3.isspace() or _is_dropped(c3):
                    if c3 in "\r\n":
                        saw_newline = True
                    j += w3
                else:
                    break
            if saw_newline and out and out[-1].isalpha() and j < n:
                cj, _ = _decode_entity_at(text, j)
                if cj.islower():
                    i = j
                    continue
            out.append("-")
            starts.append(i)
            ends.append(i + w)
            i += w
            continue
        if ch in SINGLE_QUOTES:
            out.append("'")
            starts.append(i)
            ends.append(i + w)
            i += w
            continue
        if ch in DOUBLE_QUOTES:
            out.append('"')
            starts.append(i)
            ends.append(i + w)
            i += w
            continue
        if ch == ELLIPSIS:
            for dot in "...":
                out.append(dot)
                starts.append(i)
                ends.append(i + w)
            i += w
            continue
        for f in unicodedata.normalize("NFKC", ch):
            if f.isspace():
                if out and out[-1] == " ":
                    continue
                f = " "
            out.append(f)
            starts.append(i)
            ends.append(i + w)
        i += w
    return "".join(out), starts, ends


def strip_norm(norm: str, starts: List[int], ends: List[int]
               ) -> Tuple[str, List[int], List[int]]:
    s, e = 0, len(norm)
    while s < e and norm[s] == " ":
        s += 1
    while e > s and norm[e - 1] == " ":
        e -= 1
    return norm[s:e], starts[s:e], ends[s:e]


def normalize_needle(text: str) -> str:
    norm, starts, ends = normalize_with_map(text)
    norm, _, _ = strip_norm(norm, starts, ends)
    return norm


def hyphenless_with_map(norm: str, starts: List[int], ends: List[int]
                        ) -> Tuple[str, List[int], List[int]]:
    """Remove hyphens between word characters (tier-2 matching), composing
    the span maps back to raw offsets."""
    out: List[str] = []
    out_starts: List[int] = []
    out_ends: List[int] = []
    n = len(norm)
    for k, ch in enumerate(norm):
        if ch == "-" and 0 < k < n - 1 and norm[k - 1].isalnum() and norm[k + 1].isalnum():
            continue
        out.append(ch)
        out_starts.append(starts[k])
        out_ends.append(ends[k])
    return "".join(out), out_starts, out_ends


def all_find(hay: str, needle: str) -> List[int]:
    if not needle:
        return []
    positions = []
    pos = hay.find(needle)
    while pos != -1:
        positions.append(pos)
        pos = hay.find(needle, pos + 1)
    return positions


