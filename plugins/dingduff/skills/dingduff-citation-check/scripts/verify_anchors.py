# Matching
# ---------------------------------------------------------------------------

@dataclass
class Anchor:
    quote: str
    start: int
    end: int
    match: str


@dataclass
class Failure:
    reason: str
    hint: Optional[str] = None


def _candidate_spans(doc: Document, quote: str) -> Tuple[List[Tuple[int, int, str]], bool]:
    """All match spans for a (non-ellipsis) quote across tiers, as raw
    (start, end, label) tuples in tier order. Returns (spans, any_match_at_all)
    where any_match_at_all includes matches past body_end (used to distinguish
    only_in_citing_cases from not_found)."""
    spans: List[Tuple[int, int, str]] = []
    any_match = False

    raw_positions = all_find(doc.raw, quote)
    if raw_positions:
        any_match = True
        spans = [(p, p + len(quote), "exact") for p in raw_positions]
        if any(p < doc.body_end for p, _, _ in spans):
            return spans, any_match

    norm_quote = normalize_needle(quote)
    if norm_quote:
        norm_positions = all_find(doc.norm, norm_quote)
        if norm_positions:
            any_match = True
            spans = [(*doc.raw_span(p, len(norm_quote)), "normalized") for p in norm_positions]
            if any(p < doc.body_end for p, _, _ in spans):
                return spans, any_match

        hl_doc, hl_starts, hl_ends = doc.hyphenless
        dummy = [0] * len(norm_quote)
        hl_quote, _, _ = hyphenless_with_map(norm_quote, dummy, dummy)
        if hl_quote and hl_quote != norm_quote:
            hl_positions = all_find(hl_doc, hl_quote)
            if hl_positions:
                any_match = True
                spans = [
                    (hl_starts[p], hl_ends[p + len(hl_quote) - 1], "normalized")
                    for p in hl_positions
                ]
                if any(p < doc.body_end for p, _, _ in spans):
                    return spans, any_match

    return spans, any_match


def _case_insensitive_hint(doc: Document, quote: str) -> Optional[str]:
    norm_quote = normalize_needle(quote)
    if not norm_quote:
        return None
    pos = doc.norm.lower().find(norm_quote.lower())
    if pos == -1:
        return None
    start, end = doc.raw_span(pos, len(norm_quote))
    return doc.raw[start:end]


def _prefix_shrink_hint(doc: Document, quote: str) -> Optional[str]:
    """Find the closest region by shrinking the quote prefix word-by-word;
    return ~200 raw chars of surrounding context."""
    words = normalize_needle(quote).split(" ")
    upper = min(len(words) - 1, MAX_PREFIX_SHRINK_WORDS)
    for k in range(upper, 2, -1):
        prefix = " ".join(words[:k])
        pos = doc.norm.find(prefix)
        if pos != -1:
            start, _ = doc.raw_span(pos, len(prefix))
            lo = max(0, start - 40)
            return doc.raw[lo:lo + HINT_CONTEXT_RAW_CHARS]
    return None


def anchor_plain_quote(doc: Document, quote: str) -> Tuple[Optional[List[Anchor]], Optional[Failure], List[Dict[str, str]]]:
    warnings: List[Dict[str, str]] = []
    spans, any_match = _candidate_spans(doc, quote)
    in_body = [s for s in spans if s[0] < doc.body_end]
    if in_body:
        if len(in_body) > 1:
            warnings.append({"code": "multiple_matches",
                             "detail": f"{len(in_body)} occurrences; used first in-body match"})
        start, end, label = in_body[0]
        if start < doc.header_end:
            warnings.append({"code": "match_in_header",
                             "detail": "matched text sits in the caption/metadata header"})
        if len(normalize_needle(quote)) < SHORT_QUOTE_NORM_CHARS:
            warnings.append({"code": "short_quote",
                             "detail": f"normalized quote is {len(normalize_needle(quote))} chars; ambiguity-prone"})
        return [Anchor(doc.raw[start:end], start, end, label)], None, warnings
    if any_match:
        return None, Failure("only_in_citing_cases",
                             "the quote appears only in the Citing Cases section or footer, "
                             "not in the opinion body"), warnings
    ci = _case_insensitive_hint(doc, quote)
    if ci is not None:
        return None, Failure("case_mismatch", ci), warnings
    return None, Failure("not_found", _prefix_shrink_hint(doc, quote)), warnings


def _split_ellipsis_segments(norm_quote: str) -> List[str]:
    segments = [seg.strip() for seg in norm_quote.split("...")]
    # Leading/trailing ellipses are mere trimming markers — drop empty edges.
    while segments and not segments[0]:
        segments.pop(0)
    while segments and not segments[-1]:
        segments.pop()
    return segments


def anchor_ellipsis_quote(doc: Document, quote: str, max_gap: int) -> Tuple[Optional[List[Anchor]], Optional[Failure], List[Dict[str, str]]]:
    warnings: List[Dict[str, str]] = []
    norm_quote = normalize_needle(quote)
    segments = _split_ellipsis_segments(norm_quote)
    if not segments or any(seg == "" for seg in segments):
        return None, Failure("segment_too_short",
                             "ellipsis quote contains an empty segment"), warnings
    for seg in segments:
        if len(seg) < MIN_SEGMENT_NORM_CHARS:
            return None, Failure(
                "segment_too_short",
                f"segment '{seg}' is {len(seg)} normalized chars "
                f"(minimum {MIN_SEGMENT_NORM_CHARS}) — quote more of the passage"), warnings

    first_positions = [p for p in all_find(doc.norm, segments[0])]
    in_body_firsts = [p for p in first_positions if doc.starts[p] < doc.body_end]
    if not in_body_firsts:
        if first_positions:
            return None, Failure("only_in_citing_cases",
                                 "the first segment appears only past the opinion body"), warnings
        ci = _case_insensitive_hint(doc, segments[0])
        if ci is not None:
            return None, Failure("case_mismatch", ci), warnings
        return None, Failure("not_found", _prefix_shrink_hint(doc, segments[0])), warnings

    last_failure: Failure = Failure("not_found", None)
    for start_pos in in_body_firsts[:MAX_CHAIN_START_CANDIDATES]:
        chain: List[Tuple[int, int]] = [(start_pos, start_pos + len(segments[0]))]
        ok = True
        for seg in segments[1:]:
            prev_end = chain[-1][1]
            pos = doc.norm.find(seg, prev_end)
            if pos == -1:
                anywhere = doc.norm.find(seg)
                if anywhere == -1:
                    ci = _case_insensitive_hint(doc, seg)
                    last_failure = (Failure("case_mismatch", ci) if ci is not None
                                    else Failure("not_found", _prefix_shrink_hint(doc, seg)))
                else:
                    last_failure = Failure(
                        "not_found",
                        f"segment '{seg[:60]}' occurs only before the previous segment "
                        "— ellipsis segments must appear in document order")
                ok = False
                break
            if pos - prev_end > max_gap:
                last_failure = Failure(
                    "ellipsis_gap_too_large",
                    f"gap of {pos - prev_end} normalized chars between segments exceeds {max_gap}")
                ok = False
                break
            chain.append((pos, pos + len(seg)))
        if ok:
            if len(in_body_firsts) > 1:
                warnings.append({"code": "multiple_matches",
                                 "detail": f"first segment has {len(in_body_firsts)} in-body occurrences; "
                                           "used the first that completes the chain"})
            anchors = []
            for s, e in chain:
                raw_s, raw_e = doc.raw_span(s, e - s)
                anchors.append(Anchor(doc.raw[raw_s:raw_e], raw_s, raw_e, "normalized"))
            return anchors, None, warnings
    return None, last_failure, warnings


def anchor_quote(doc: Document, quote: str, max_gap: int):
    if "..." in quote or ELLIPSIS in quote:
        return anchor_ellipsis_quote(doc, quote, max_gap)
    return anchor_plain_quote(doc, quote)


def anchor_memo(memo: Document, cite_text: str, memo_context: Optional[str]):
    """Locate cite_text in the memo; returns ((start, end), failure)."""
    norm_cite = normalize_needle(cite_text)
    if not norm_cite:
        return None, Failure("not_found", "cite_text is empty after normalization")
    positions = all_find(memo.norm, norm_cite)
    if len(positions) == 1:
        return memo.raw_span(positions[0], len(norm_cite)), None
    if not positions:
        ci = _case_insensitive_hint(memo, cite_text)
        if ci is not None:
            return None, Failure("case_mismatch", ci)
        return None, Failure("not_found",
                             "cite_text does not appear in the memo — it must match the memo verbatim")
    if not memo_context:
        return None, Failure("ambiguous_in_memo",
                             f"cite_text occurs {len(positions)} times; provide a memo_context snippet")
    norm_ctx = normalize_needle(memo_context)
    ctx_positions = all_find(memo.norm, norm_ctx)
    if not ctx_positions:
        return None, Failure("memo_context_not_found",
                             "memo_context does not appear in the memo verbatim")
    if len(ctx_positions) > 1:
        return None, Failure("ambiguous_in_memo",
                             f"memo_context occurs {len(ctx_positions)} times; extend it until unique")
    cs = ctx_positions[0]
    ce = cs + len(norm_ctx)
    inner = memo.norm.find(norm_cite, cs, ce)
    if inner == -1:
        return None, Failure("cite_text_not_in_context",
                             "cite_text does not occur inside memo_context")
    return memo.raw_span(inner, len(norm_cite)), None


