@dataclass
class Document:
    raw: str
    norm: str = field(init=False)
    starts: List[int] = field(init=False)
    ends: List[int] = field(init=False)
    body_end: int = field(init=False)
    header_end: int = field(init=False)
    _hyphenless: Optional[Tuple[str, List[int], List[int]]] = field(default=None, init=False)

    def __post_init__(self) -> None:
        self.norm, self.starts, self.ends = normalize_with_map(self.raw)
        m = BODY_END_RE.search(self.raw) or FOOTER_RE.search(self.raw)
        self.body_end = m.start() if m else len(self.raw)
        sep = self.raw.find("\n---\n")
        self.header_end = sep if sep != -1 else 0

    @property
    def hyphenless(self) -> Tuple[str, List[int], List[int]]:
        if self._hyphenless is None:
            self._hyphenless = hyphenless_with_map(self.norm, self.starts, self.ends)
        return self._hyphenless

    def raw_span(self, norm_start: int, norm_len: int) -> Tuple[int, int]:
        return self.starts[norm_start], self.ends[norm_start + norm_len - 1]


def read_document(path: Path) -> str:
    """Decode UTF-8 bytes directly — never text mode, so newline translation
    can never shift offsets."""
    return path.read_bytes().decode("utf-8")


def sha256_file(path: Path) -> str:
    return hashlib.sha256(path.read_bytes()).hexdigest()


