class FatalError(Exception):
    pass


SOURCE_PASSTHROUGH_KEYS = (
    "type", "path", "cluster_id", "case_name", "citation",
    "code", "section", "statute_id", "jurisdiction", "missing",
    "title", "kind",
)

# "opinion"/"statute" are public hosted authorities the review panel re-fetches
# from DingDuff. A "document" is an attorney-supplied working file (a
# Restatement section, an off-CourtListener case, an opposing brief, a factual
# PDF): matched the same way, but it has no citing-cases appendix to guard and
# is delivered to the panel inline rather than fetched.
SOURCE_TYPES = ("opinion", "statute", "document")

CLUSTER_ID_LINE_RE = re.compile(r"^\*\*Cluster ID:\*\*\s*(\S+)", re.MULTILINE)


def validate_proposals(proposals: Any) -> None:
    if not isinstance(proposals, dict):
        raise FatalError("proposals must be a JSON object")
    if proposals.get("schema_version") != SCHEMA_VERSION:
        raise FatalError(
            f"proposals.schema_version must be {SCHEMA_VERSION}")
    memo = proposals.get("memo")
    if not isinstance(memo, dict) or not isinstance(memo.get("path"), str):
        raise FatalError("proposals.memo.path is required")
    sources = proposals.get("sources")
    if not isinstance(sources, dict) or not sources:
        raise FatalError("proposals.sources must be a non-empty object")
    for key, src in sources.items():
        if not isinstance(src, dict) or src.get("type") not in SOURCE_TYPES:
            raise FatalError(
                f"source '{key}': type must be one of {SOURCE_TYPES}")
        if src.get("missing"):
            continue
        if not isinstance(src.get("path"), str):
            raise FatalError(f"source '{key}': path is required unless missing=true")
    citations = proposals.get("citations")
    if not isinstance(citations, list) or not citations:
        raise FatalError("proposals.citations must be a non-empty list")
    seen_ids = set()
    for c in citations:
        if not isinstance(c, dict):
            raise FatalError("each citation must be an object")
        cid = c.get("id")
        if not isinstance(cid, str) or not CITATION_ID_RE.match(cid):
            raise FatalError(f"citation id '{cid}' must match c<NNN> (e.g. c001)")
        if cid in seen_ids:
            raise FatalError(f"duplicate citation id '{cid}'")
        seen_ids.add(cid)
        if c.get("source") not in sources:
            raise FatalError(f"citation '{cid}': unknown source '{c.get('source')}'")
        if not isinstance(c.get("cite_text"), str) or not c["cite_text"].strip():
            raise FatalError(f"citation '{cid}': cite_text is required")
        if not isinstance(c.get("proposition"), str) or not c["proposition"].strip():
            raise FatalError(f"citation '{cid}': proposition is required")
        if c.get("support_type") not in ("quotation", "paraphrase"):
            raise FatalError(f"citation '{cid}': support_type must be 'quotation' or 'paraphrase'")
        anchors = c.get("anchors_proposed", [])
        if c.get("no_quote_claimed"):
            continue
        if (not isinstance(anchors, list) or not anchors
                or not all(isinstance(a, dict) and isinstance(a.get("quote"), str)
                           and a["quote"].strip() for a in anchors)):
            raise FatalError(
                f"citation '{cid}': anchors_proposed must contain at least one quote "
                "(or set no_quote_claimed=true)")


def utc_now_iso() -> str:
    return datetime.now(timezone.utc).isoformat(timespec="seconds").replace("+00:00", "Z")


def run(proposals: Dict[str, Any], workdir: Path, max_gap: int,
        generated_by: Optional[str]) -> Tuple[Dict[str, Any], Dict[str, Any]]:
    """Process proposals; returns (cites_json, report)."""
    memo_path = workdir / proposals["memo"]["path"]
    if not memo_path.is_file():
        raise FatalError(f"memo file not found: {proposals['memo']['path']}")
    try:
        memo_doc = Document(read_document(memo_path))
    except UnicodeDecodeError as exc:
        raise FatalError(f"memo is not valid UTF-8: {exc}") from exc
    memo_doc.body_end = len(memo_doc.raw)  # memos have no citing-cases section
    memo_doc.header_end = 0

    warnings: List[Dict[str, str]] = []
    docs: Dict[str, Document] = {}
    out_sources: Dict[str, Dict[str, Any]] = {}
    for key, src in proposals["sources"].items():
        entry = {k: src[k] for k in SOURCE_PASSTHROUGH_KEYS if k in src}
        if src.get("missing"):
            entry["path"] = None
            entry["sha256"] = None
            entry["missing"] = True
            out_sources[key] = entry
            continue
        path = workdir / src["path"]
        if not path.is_file():
            raise FatalError(f"source '{key}' file not found: {src['path']}")
        try:
            docs[key] = Document(read_document(path))
        except UnicodeDecodeError as exc:
            raise FatalError(f"source '{key}' is not valid UTF-8: {exc}") from exc
        if src.get("type") == "document":
            # A working file has no citing-cases appendix or DingDuff footer;
            # the whole file is quotable. (Opinions keep the body guard; the
            # `## Citing Cases` / footer markers gate quoting to the body.)
            docs[key].body_end = len(docs[key].raw)
            docs[key].header_end = 0
        entry["sha256"] = sha256_file(path)
        if src.get("type") == "opinion" and src.get("cluster_id") is not None:
            m = CLUSTER_ID_LINE_RE.search(docs[key].raw)
            if m and m.group(1) != str(src["cluster_id"]):
                warnings.append({"id": key, "code": "cluster_id_mismatch",
                                 "detail": f"file says cluster {m.group(1)}, "
                                           f"proposal says {src['cluster_id']}"})
        out_sources[key] = entry

    out_citations: List[Dict[str, Any]] = []
    failed: List[Dict[str, Any]] = []
    unverifiable: List[Dict[str, Any]] = []
    by_status: Dict[str, int] = {"anchored": 0, "anchor_failed": 0, "no_quote_claimed": 0}

    for c in proposals["citations"]:
        cid = c["id"]
        source_key = c["source"]
        cite_warnings: List[Dict[str, str]] = []

        memo_anchor, memo_failure = anchor_memo(memo_doc, c["cite_text"], c.get("memo_context"))
        if memo_failure:
            failed.append({"id": cid, "source": source_key, "where": "memo",
                           "quote": c["cite_text"], "reason": memo_failure.reason,
                           "hint": memo_failure.hint})

        anchors: List[Anchor] = []
        source_failure_reason: Optional[str] = None
        src_entry = out_sources[source_key]
        if src_entry.get("missing"):
            source_failure_reason = "source_missing"
        elif not c.get("no_quote_claimed"):
            doc = docs[source_key]
            for proposed in c.get("anchors_proposed", []):
                quote = proposed["quote"]
                got, failure, w = anchor_quote(doc, quote, max_gap)
                for item in w:
                    cite_warnings.append({"id": cid, **item})
                if failure:
                    source_failure_reason = source_failure_reason or failure.reason
                    failed.append({"id": cid, "source": source_key, "where": "source",
                                   "quote": quote, "reason": failure.reason,
                                   "hint": failure.hint})
                else:
                    anchors.extend(got)

        if src_entry.get("missing"):
            status, failure_reason = "anchor_failed", "source_missing"
            unverifiable.append({"id": cid, "source": source_key, "reason": "source_missing"})
        elif memo_failure or source_failure_reason:
            status = "anchor_failed"
            failure_reason = source_failure_reason or memo_failure.reason
        elif c.get("no_quote_claimed"):
            status, failure_reason = "no_quote_claimed", None
        else:
            status, failure_reason = "anchored", None
        by_status[status] += 1

        entry: Dict[str, Any] = {
            "id": cid,
            "source": source_key,
            "cite_text": c["cite_text"],
            "memo_anchor": ({"start": memo_anchor[0], "end": memo_anchor[1]}
                            if memo_anchor else None),
            "proposition": c["proposition"],
            "support_type": c["support_type"],
            "anchors": [vars(a) for a in anchors],
            "status": status,
            # Review-binding hash: verdicts in review.json bind to this value,
            # so a verdict goes stale when the cite or proposition changes.
            "binds_to": hashlib.sha256(
                (c["cite_text"] + "\x00" + c["proposition"]).encode("utf-8")
            ).hexdigest(),
        }
        if c.get("pin") is not None:
            entry["pin"] = c["pin"]
        if failure_reason:
            entry["failure_reason"] = failure_reason
        out_citations.append(entry)
        warnings.extend(cite_warnings)

    generated_by_full = (f"{generated_by} ({SCRIPT_VERSION})" if generated_by
                         else SCRIPT_VERSION)
    cites = {
        "schema_version": SCHEMA_VERSION,
        "memo": {
            "path": proposals["memo"]["path"],
            "sha256": sha256_file(memo_path),
            "generated_at": utc_now_iso(),
            "generated_by": generated_by_full,
        },
        "sources": out_sources,
        "citations": out_citations,
    }
    report = {
        "ok": not failed,
        "citations_total": len(out_citations),
        "anchored": by_status["anchored"],
        "by_status": by_status,
        "failed": failed,
        "unverifiable": unverifiable,
        "warnings": warnings,
    }
    return cites, report


def main(argv: Optional[List[str]] = None) -> int:
    parser = argparse.ArgumentParser(description=__doc__.splitlines()[0])
    parser.add_argument("--proposals", required=True, help="path to proposals.json")
    parser.add_argument("--out", required=True, help="path to write cites.json")
    parser.add_argument("--workdir", default=".",
                        help="directory against which relative paths resolve")
    parser.add_argument("--max-gap", type=int, default=2000,
                        help="max normalized-char gap between ellipsis segments")
    parser.add_argument("--generated-by", default=None,
                        help="label recorded in cites.json memo.generated_by")
    args = parser.parse_args(argv)

    workdir = Path(args.workdir)
    try:
        try:
            proposals = json.loads((workdir / args.proposals).read_text(encoding="utf-8"))
        except (OSError, json.JSONDecodeError) as exc:
            raise FatalError(f"cannot read proposals: {exc}") from exc
        validate_proposals(proposals)
        cites, report = run(proposals, workdir, args.max_gap, args.generated_by)
    except FatalError as exc:
        print(f"fatal: {exc}", file=sys.stderr)
        return 2

    out_path = workdir / args.out
    out_path.write_text(json.dumps(cites, ensure_ascii=False, indent=2) + "\n",
                        encoding="utf-8")
    print(json.dumps(report, ensure_ascii=False, indent=2))
    return 0 if report["ok"] else 1


if __name__ == "__main__":
    sys.exit(main())
